while True:
    print("\n===== MENU =====")
    print("1. Bài 1")
    print("2. Bài 2")
    print("3. Bài 3")
    print("4. Bài 4")
    print("0. Thoát")

    chon = int(input("Chọn chương trình: "))

    if chon == 1:
        print("Bạn chọn Bài 1")

    elif chon == 2:
        print("Bạn chọn Bài 2")

    elif chon == 3:
        print("Bạn chọn Bài 3")

    elif chon == 4:
        print("Bạn chọn Bài 4")

    elif chon == 0:
        print("Thoát chương trình")
        break

    else:
        print("Lựa chọn không hợp lệ!")

